const StockChart_Data = () => {
    const [Data, setData] = useState(lastDay)

    const handleDataChange = (e) => {
        e.preventDefault();
        let val = e.target.innerHTML;        
        switch (val) {
            case 'Day':
                setData(lastDay)
                break;
            case 'Week':
                setData(last7Days)
                break;
            case 'Month':
                setData(lastMonth)
                break;
            case 'Year':
                setData(lastYear)
                break;
            default:
                break;
        } 

    }



    return (
        <div className='chartData'>
            <ResponsiveContainer width= {'99%'} height={500}>
                <LineChart data={Data}>
                    <Line type="monotone" dataKey="value" stroke="#8884d8" />
                    <XAxis dataKey="date" color="black" fontFamily="sans-serif" stroke="white" />
                    <YAxis width={40} stroke="white"/>
                    <Tooltip/>
                </LineChart>

            </ResponsiveContainer>

            {/**Buttons to adjust length of time for data */}

            <div className='chartButtons'>
                <button onClick={handleDataChange}>Day</button>
                <button onClick={handleDataChange}>Week</button>
                <button onClick={handleDataChange}>Month</button>
                <button onClick={handleDataChange}>Year</button>
            </div>
        </div>
    )

}



